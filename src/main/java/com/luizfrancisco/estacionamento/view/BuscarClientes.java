        DefaultTableModel model = (DefaultTableModel) tblBuscarClientes.getModel();
        model.setNumRows(0);

        // 4. Preenche a tabela com a lista resultante (filtrada ou completa)
        for (Cliente c : cDAO.buscar(buscar)){
            model.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getEmail(),
                c.getTelefone(),
            });
        }       
